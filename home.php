                    <div class="intro-text">
                        <p style="text-align: justify">Dr. John L. Holland was an academic psychologist best known for developing the most widely used system of career classification used today, the Holland code or RIASEC system. </br></br>Dr. Holland's theory proposes that there are six broad areas into which all careers can be classified, and which can describe people and their interests as well. Determining a person's top interest area allows him or her to easily find careers that will match those interests.</p></br></br>
						
						<p style="text-align: justify">THE 6 INTEREST AREA</br></br>Each of the 6 interest areas describes a broad field of similar work tasks and activities. Interest areas are also descriptive of people: their values, motivations, and preferences. For each interest area, there is a collection of typical job tasks, as well as a description of the type of person who would be interested in doing those sorts of tasks. </br></br>Most professional career advisors are familiar with the system of Holland codes or RIASEC. RIASEC is an acronym for Holland's original 6 types. Because the names of Holland's 6 types may not be obvious to non-professionals, we use a slightly different terminology which is more self-explanatory.</p></br></br>
						
						<p style="text-align: justify">RIASEC Defined </br></br>Realistic (R)  Likes to work with animals, tools, or machines  Ggenerally avoids social activities like teaching, healing, and informing others  Has good skills in working with tools, mechanical or electrical drawings, machines, or plants and animals  Values practical things you can see, touch, and use like plants and animals, tools, equipment, or machines  Sees self as practical, mechanical, and realistic.</br></br>Investigative (I)  Likes to study and solve math or science problems  Generally avoids leading, selling, or persuading people  Is good at understanding and solving science and math problems  Values science  Sees self as precise, scientific, and intellectual </br></br>Artistic (A)  Likes to do creative activities like art, drama, crafts, dance, music, or creative writing  Generally avoids highly ordered or repetitive activities  Has good artistic abilities -- in creative writing, drama, crafts, music, or art  Values the creative arts -- like drama, music, art, or the works of creative writers  Sees self as expressive, original, and independent </br></br>Social (S)  Likes to do things to help people -- like, teaching, nursing, or giving first aid, providing information  Generally avoids using machines, tools, or animals to achieve a goal  Is good at teaching, counseling, nursing, or giving information  Values helping people and solving social problems  Sees self as helpful, friendly, and trustworthy </br></br>Enterprising (E)  Likes to lead and persuade people, and to sell things and ideas   Generally avoids activities that require careful observation and scientific, analytical thinking  Is good at leading people and selling things or ideas  Values success in politics, leadership, or business  Sees self as energetic, ambitious, and sociable </br></br>Conventional (C)  Likes to work with numbers, records, or machines in a set, orderly way  Generally avoids ambiguous, unstructured activities  Is good at working with written records and numbers in a systematic, orderly way  Values success in business  Sees self as orderly, and good at following a set plan </p>
